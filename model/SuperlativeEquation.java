public class SuperlativeEquation {    
    protected double b, c;
    
    public SuperlativeEquation()
    {
    }
    
    public SuperlativeEquation(double b, double c)
    {
        this.b = b;
        this.c = c;
    }

    public void setB(double b) {
        this.b = b;
    }

    public void setC(double c) {
        this.c = c;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }  

    
}
